package com.ticoderdev.central_de_tecnologia.model;

public class ModeloTelefone {
    private String modelo;
    private String marca;
    private String tela;
    private String memoria;
    private String camera;
    private String bateria;
    private String precoTela;
    private String precoBateria;
    private String precoPlaca;

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTela() {
        return tela;
    }

    public void setTela(String tela) {
        this.tela = tela;
    }

    public String getMemoria() {
        return memoria;
    }

    public void setMemoria(String memoria) {
        this.memoria = memoria;
    }

    public String getCamera() {
        return camera;
    }

    public void setCamera(String camera) {
        this.camera = camera;
    }

    public String getBateria() {
        return bateria;
    }

    public void setBateria(String bateria) {
        this.bateria = bateria;
    }

    public String getPrecoTela() {
        return precoTela;
    }

    public void setPrecoTela(String precoTela) {
        this.precoTela = precoTela;
    }

    public String getPrecoBateria() {
        return precoBateria;
    }

    public void setPrecoBateria(String precoBateria) {
        this.precoBateria = precoBateria;
    }

    public String getPrecoPlaca() {
        return precoPlaca;
    }

    public void setPrecoPlaca(String precoPlaca) {
        this.precoPlaca = precoPlaca;
    }

}
